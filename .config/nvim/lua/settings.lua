vim.cmd("colorscheme nordfox")

-- mouse
vim.cmd[[
  set mouse=a
]]

-- multiple files
vim.cmd [[
  set splitbelow
  set splitright
  set equalalways!
]]

-- tabs and indenting
vim.cmd [[
  set tabstop=2
  set shiftwidth=2
  set smarttab
  set autoindent
  set smartindent
  set expandtab
]]

-- editor
vim.cmd [[
  set number
]]

